// This program calculates and displays .
// the area and perimeter of the rectangle.
// The rectangle has a length of 12cm and width of 6cm

#include <iostream>
#include <cmath>

int main() {
// The code below illustrates the length and width of the rectangle
    std::cout << "A rectangle has length of 12cm and width of 6cm" << std::endl;
// The code below demonstrates the steps to find and solve
// the area of the rectangle with the given dimensions
    std::cout << "Area :" << (12 * 6) << "cm^2" << std::endl;
// The code below demonstrates the steps to find and solve
// the perimeter of the rectangle with the given dimensions
    std::cout << "Perimeter :" << ((12 + 12) + (6 + 6)) << "cm" << std::endl;
}
